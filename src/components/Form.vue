<template>
  <div>
    <b-form @submit="onSubmit" @reset="onReset" v-if="show">
      <b-form-group
        id="input-group-1"
        label="Email address:"
        label-for="input-1"
      >
        <b-form-input
          id="input-1"
          v-model="form.email"
          type="email"
          placeholder="Enter email"
          required
        ></b-form-input>
      </b-form-group>

      <b-form-group id="input-group-2" label="Your Name:" label-for="input-2">
        <b-form-input
          id="input-2"
          v-model="form.name"
          placeholder="Enter name"
          required
        ></b-form-input>
      </b-form-group>

      <b-form-group id="input-group-3" label="Service:" label-for="input-3">
        <b-form-select
          id="input-3"
          v-model="form.service"
          :options="service"
          required
        ></b-form-select>
      </b-form-group>


    <b-form-group
        id="input-group-4"
        label="Notes:"
        label-for="input-4"
      >
        <b-form-textarea
        id="input-4"
        v-model="text"
        placeholder="Enter any additional notes..."
        rows="3"
        max-rows="6"
        ></b-form-textarea>
      </b-form-group>
    

      <b-button type="submit" variant="primary">Submit</b-button>
    </b-form>
    <b-card class="mt-3" header="Form Data Result">
      <pre class="m-0">{{ form }}</pre>
    </b-card>
  </div>
</template>

<script>
  export default {
    data() {
      return {
        form: {
          email: '',
          name: '',
          service: null,
          text: ''
        },
        service: [{ text: 'Select One', value: null }, 'Tire Repair', 'Inner Tube Repair', 'Gear Replacement', 'Gear Adjustment', 'Brake Pad Reapir', 'Brake Cable Repair', 'Chain Tensioning', 'Chain Repair', 'Full Maintenance'], 
        show: true
      }
    },
    methods: {
      onSubmit(event) {
        event.preventDefault()
        alert(JSON.stringify(this.form))
      },
      onReset(event) {
        event.preventDefault()
        // Reset our form values
        this.form.email = ''
        this.form.name = ''
        this.form.service = null
        this.form.text = ''
        // Trick to reset/clear native browser form validation state
        this.show = false
        this.$nextTick(() => {
          this.show = true
        })
      }
    }
  }
</script>